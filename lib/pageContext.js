export function pageDescription(pathRaw) {
  if (!pathRaw) return null;
  const path = String(pathRaw).split("?")[0].replace(/\/+$/, "") || "/";
  const first = "/" + (path.split("/")[1] || "");
  const desc = MAP[path] || MAP[first];
  return { path, desc: desc || `「${path}」页面(具体样子你按常识判断)` };
}
